print("---- Day0003: 関数の導入 practice ----")

# -------------------------------------
# ① 関数とは？
# → 「何度も使う処理に名前をつけておく仕組み」
# -------------------------------------

def say_hello():
    print("Hello! This is a function.")

# 関数の呼び出し
say_hello()


# -------------------------------------
# ② 引数つきの関数
# → 「入力を受け取って動く関数」
# -------------------------------------

def greet(name):
    print("こんにちは、", name, "さん！")

greet("kabo")
greet("mikoto")


# -------------------------------------
# ③ return つきの関数
# → 「値を計算して、結果を返す」
# -------------------------------------

def add(a, b):
    return a + b

result = add(5, 7)
print("5 + 7 の結果は:", result)


# -------------------------------------
# ④ 関数の役割まとめ
# - 覚えやすい名前をつける
# - 処理をひとまとめにできる
# - 他の場所でも何回も呼び出せる
# - コードが読みやすくなる
