print("最後の要素:", fruits[-1])
print("\n")
# ------------------------------
# Day9-002: list の編集（追加・削除）
# ------------------------------
# ☆ この項目でやっていること
# - list に要素を「足す」「消す」「取り出しながら消す」を練習
# - よく使うメソッド3つ：
#   - append(x) : 一番うしろに x を追加
#   - remove(x) : 最初に見つかった x を削除
#   - pop()     : 一番うしろの要素を取り出して、ついでに削除
# ☆ こんな場面で使うイメージ
# - 在庫に商品が増えた/売れて減ったときの更新
# - 一時的な「やることリスト（TODO）」の追加・消し込み
# - 最後のデータだけ取り出して処理する

print("---- Day9-002: list 編集 ----")

fruits = ["apple", "banana", "orange"]
print("最初:", fruits)

#要素を追加
fruits.append("grape")
print("append後:", fruits)

#特定の要素を削除
fruits.remove("banana")
print("remove後:", fruits)

#最後の要素を取り出しつつ削除
last = fruits.pop()
print("popされた要素:", last)
print("pop後:", fruits)
print("\n")
