# Day9-001: list の基本
# ------------------------------
# ☆ この項目でやっていること
# - 「フルーツの名前」を1つのまとまりとして list に入れる
# - len(...) で「いくつ入っているか」を数える
# - インデックス（番号）で要素を取り出す
#   - 0番目 → 先頭の要素
#   - -1番目 → 最後の要素
# ☆ こんな場面で使うイメージ
