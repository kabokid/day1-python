# Day9-005: dict を使ったカウント集計ミニ課題
# ------------------------------
# ☆ この項目でやっていること
# - 名前のリスト（アクセスログ）から「誰が何回アクセスしたか」を集計する
# - dict を「名前 → カウント回数」の対応表として使う
# - キーがまだ無いときは1で初期化、あるときは +1 していく
# ☆ こんな場面で使うイメージ
# - 「ユーザーごとのログイン回数」を数える
# - 「エラーコードごとの発生回数」を数える
# - 「商品IDごとの購入回数」を集計する
print("---- Day9-005: アクセス回数の集計 ----")

logs = ["kabo", "mikoto", "kabo", "gonzou", "kabo", "mikoto"]

access_count = {} #名前　→　回数を入れるdict

for name in logs:
    if name in access_count:
        #すでにキーがある　→　カウントを1増やす
        access_count[name] += 1
    else:
        #まだキーがない　→　初回なので1にする
        access_count[name] = 1

print("アクセス回数の集計結果:", access_count)